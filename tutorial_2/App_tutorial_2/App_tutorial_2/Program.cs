// Character escape sequences and textual strings tutorial

Console.WriteLine("Hello\nWorld!");
Console.WriteLine("My \nName \nis \nMarcus");
Console.WriteLine("Hi\nMyn friend!");

Console.WriteLine("tab1 \ttab 2 \t tab3 \t tab4");

// just to see the behavior of use \n and \t inside strings
// the \n sequence will add a new line, and a \t sequence will add a tab.

// other exemples

// this is to use double-quotation  (") insert a \"
Console.WriteLine("Hello \"World\"!");